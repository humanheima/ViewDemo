@Composable
fun ProgressIndicatorPractice(
    modifier: Modifier,
    onBackClick: () -> Unit = {}
) {
    Scaffold(modifier,
        topBar = { SimpleTopAppBarExample("测试Compose ProgressIndicator", onBackClick) }
    ) { padding ->

        //获取Context
        val context = LocalContext.current
        Column(modifier = modifier.padding(padding)) {

            LinearDeterminateIndicatorSample()

            CircleDeterminateIndicatorExample()

            IndeterminateLinearIndicatorExample()

            IndeterminateCircularIndicatorExample()

        }
    }
}

@Composable
private fun LinearDeterminateIndicatorSample() {
    var currentProgress by remember { mutableFloatStateOf(0f) }
    var loading by remember { mutableStateOf(false) }
    val scope = rememberCoroutineScope() // Create a coroutine scope

    Column(
        verticalArrangement = Arrangement.spacedBy(12.dp),
        horizontalAlignment = Alignment.CenterHorizontally,
        modifier = Modifier.fillMaxWidth()
    ) {
        Button(onClick = {
            loading = true
            scope.launch {
                loadProgress { progress ->
                    currentProgress = progress
                }
                loading = false // Reset loading when the coroutine finishes
            }
        }, enabled = !loading) {
            Text("Start loading")
        }

        if (loading) {
            LinearProgressIndicator(
                progress = currentProgress,
                modifier = Modifier.fillMaxWidth(),
                //color = MaterialTheme.colorScheme.primary,
                //trackColor = MaterialTheme.colorScheme.tertiary
            )
        }
    }
}

@Composable
private fun CircleDeterminateIndicatorExample() {
    var currentProgress by remember { mutableFloatStateOf(0f) }
    var loading by remember { mutableStateOf(false) }
    val scope = rememberCoroutineScope() // Create a coroutine scope

    Column(
        verticalArrangement = Arrangement.spacedBy(12.dp),
        horizontalAlignment = Alignment.CenterHorizontally,
        modifier = Modifier.fillMaxWidth()
    ) {
        Button(onClick = {
            loading = true
            scope.launch {
                loadProgress { progress ->
                    currentProgress = progress
                }
                loading = false // Reset loading when the coroutine finishes
            }
        }, enabled = !loading) {
            Text("Start loading")
        }

        if (loading) {
            CircularProgressIndicator(
                progress = currentProgress,
                modifier = Modifier
                    .width(120.dp)
                    .height(120.dp),
                color = MaterialTheme.colorScheme.primary,
                //trackColor = MaterialTheme.colorScheme.tertiary
            )
        }
    }
}

/** Iterate the progress value */
suspend fun loadProgress(updateProgress: (Float) -> Unit) {
    for (i in 1..100) {
        updateProgress(i.toFloat() / 100)
        delay(50)
    }
}

@Composable
private fun IndeterminateCircularIndicatorExample() {
    var loading by remember { mutableStateOf(false) }

    Button(onClick = { loading = true }, enabled = !loading) {
        Text("Start loading")
    }

    if (!loading) return

    CircularProgressIndicator(
        modifier = Modifier.width(64.dp),
        color = MaterialTheme.colorScheme.secondary,
        trackColor = MaterialTheme.colorScheme.surfaceVariant,
    )
}

